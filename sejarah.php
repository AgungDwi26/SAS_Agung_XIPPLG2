                    <h1>RAPAT PERTAMA, GEDUNG KATHOLIEKE JONGENLINGEN BOND
                        </h1>
                        <h5><i>“Perceraiberaian itu wajiblah diperangi, agar kita bisa bersatu” (Sambutan Sugondo Djojopuspto dalam pembukaan kongres)."</i></h5>
                        <p>Rapat pertama, malam hari Sabtu, 27 Oktober 1928, di Gedung Katholieke Jongenlingen Bond (KJB), Ketua Kongres, Sugondo Djojopuspito, memberi sambutan. Ia berharap kongres ini dapat memperkuat semangat persatuan dalam sanubari para pemuda. Acara dilanjutkan dengan uraian Mohammad Yamin tentang arti dan hubungan persatuan dengan pemuda. Menurutnya, ada lima faktor yang bisa memperkuat persatuan Indonesia yaitu sejarah, bahasa, hukum adat, pendidikan, dan kemauan.</p>
